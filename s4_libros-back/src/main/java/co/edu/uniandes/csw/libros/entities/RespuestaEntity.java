package co.edu.uniandes.csw.libros.entities;

import java.io.Serializable;
import java.util.Date;
import javax.persistence.Entity;
import javax.persistence.FetchType;
import javax.persistence.OneToOne;
import javax.persistence.Temporal;
import javax.persistence.TemporalType;
import uk.co.jemos.podam.common.PodamExclude;

/**
 *
 * @author diegogomez
 */
@Entity
public class RespuestaEntity extends BaseEntity implements Serializable {

    private String razon;

    private Integer calificacion;

    private String comentario;

    @Temporal(TemporalType.DATE)
    private Date fechaEnvio;

    
    @PodamExclude
    @OneToOne(mappedBy = "respuesta", fetch = FetchType.LAZY)
    private CanjeEntity canje;
    
    
    public RespuestaEntity(){}

    /**
     * @return the razon
     */
    public String getRazon() {
        return razon;
    }

    /**
     * @param razon the razon to set
     */
    public void setRazon(String razon) {
        this.razon = razon;
    }

    /**
     * @return the calificacion
     */
    public Integer getCalificacion() {
        return calificacion;
    }

    /**
     * @param calificacion the calificacion to set
     */
    public void setCalificacion(Integer calificacion) {
        this.calificacion = calificacion;
    }

    /**
     * @return the comentario
     */
    public String getComentario() {
        return comentario;
    }

    /**
     * @param comentario the comentario to set
     */
    public void setComentario(String comentario) {
        this.comentario = comentario;
    }

    /**
     * @return the fechaEnvio
     */
    public Date getFechaEnvio() {
        return fechaEnvio;
    }

    /**
     * @param fechaEnvio the fechaEnvio to set
     */
    public void setFechaEnvio(Date fechaEnvio) {
        this.fechaEnvio = fechaEnvio;
    }


    
    public CanjeEntity getCanje() {
        return canje;
    }

    public void setCanje(CanjeEntity canje) {
        this.canje = canje;
    }
    
    
}
